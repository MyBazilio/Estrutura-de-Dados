using System;
class Relational {
    static void Main() {
        int x = 10, y = 20;
        Console.WriteLine($"x == y -> {x == y}");
        Console.WriteLine($"x != y -> {x != y}");
        Console.WriteLine($"x < y  -> {x < y}");
        Console.WriteLine($"x <= y -> {x <= y}");
        Console.WriteLine($"x > y  -> {x > y}");
        Console.WriteLine($"x >= y -> {x >= y}");
    }
}